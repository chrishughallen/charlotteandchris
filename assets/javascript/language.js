const faqContainer = document.getElementById('faq');

document.querySelectorAll('[data-lang]').forEach(btn => {
  btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
});

function setLanguage(lang) {
  document.cookie = `lang=${lang}; path=/; max-age=${60 * 60 * 24 * 30}`;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    el.textContent = translations[key]?.[lang] || '';
  });

  updateFaq(lang)
}

function updateFaq(lang) {
  faqContainer.innerHTML = '';
  Object.values(faqs).forEach(faq => {
    const faqItem = document.createElement('div');
    const question = document.createElement('div');
    const answer = document.createElement('div');
    question.className = 'title',
    question.textContent = faq[lang].q;
    answer.textContent = faq[lang].a;
    faqItem.appendChild(question)
    faqItem.appendChild(answer)
    faqContainer.appendChild(faqItem)
  })
}

// Read cookie helper
function getCookie(name) {
  const match = document.cookie.match(new RegExp('(^| )' + name + '=([^;]+)'));
  return match ? match[2] : null;
}

// On page load
const savedLang = getCookie("lang") || "fr";
setLanguage(savedLang);
